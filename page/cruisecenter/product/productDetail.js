var pageData = {}
pageData.data = {
        img: ['1', '0', '02', '01', '2', '3'],
        indicatorDots: false,
        vertical: false,
        autoplay: true,
        interval: 3000,
        duration: 1000,
        circular: true
}

pageData.widgetsToggle = function (e) {
    var id = e.currentTarget.id, data = {};
    data[id + 'Show'] = !this.data[id + 'Show'];
    this.setData(data);
}

var toastNum = 1

for(var i = 0; i <= toastNum; ++i) {
  pageData.data['toast'+i+'Hidden'] = true;
  (function (index) {
    pageData['toast'+index+'Change'] = function(e) {
      var obj = {}
      obj['toast'+index+'Hidden'] = true;
      this.setData(obj)
    }
    pageData['toast'+index+'Tap'] = function(e) {
      var obj = {}
      obj['toast'+index+'Hidden'] = false
      this.setData(obj)
    }
  })(i)
}

pageData.onLoad = function(options) {
    console.log(options)
    var id = options.id
    var that = this
  
    wx.request({
        url: 'https://service.huiyoulun.com/service/getProductById', 
        method: 'POST',
        data: {
           id:id
        },
        header: {
            'content-type': 'application/json'
        },
        success: function(res) {
            console.log(res.data)
            var data = res.data;
           
            //签证说明中<br/>换成\n
            var o = data.product.visaComment;
            data.product.visaComment = o.replace(/<br\/>/g,'\n\n');
            that.setData({
                pro:data
            })
        },
        fail: function(err) {
            console.log(err)
        }
    })

    //设置产品基本情况默认打开状态
    var id = 'view', data = {};
    data[id + 'Show'] = !this.data[id + 'Show'];
    this.setData(data);
}

Page(pageData)

